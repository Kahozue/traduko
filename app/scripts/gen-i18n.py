def generate(name: str, table: dict, export_name: str) -> None:
    source = SRC.read_text(encoding="utf-8")
    keys = re.findall(r'^\s*"((?:[^"\\]|\\.)+)":', source, re.M)
    missing = [key for key in keys if key not in table]
    extra = [key for key in table if key not in keys]
    if missing or extra:
        raise SystemExit(f"{name}: missing={missing[:10]} extra={extra[:10]} (missing {len(missing)}, extra {len(extra)})")
    lines = [
        "// GENERATED from zh-TW key order by scripts/gen-i18n.py; edit the",
        "// translation tables there, not this file directly.",
        'import type { MessageKey } from "./index";',
        "",
        f"export const {export_name}: Record<MessageKey, string> = {{",
    ]
    for key in keys:
        lines.append(f"  {json.dumps(key, ensure_ascii=False)}: {json.dumps(table[key], ensure_ascii=False)},")
    lines.append("};")
    (ROOT / "src" / "i18n" / name).write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"{name}: {len(keys)} keys")


